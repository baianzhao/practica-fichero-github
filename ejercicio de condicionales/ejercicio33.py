#Programa un código que permita contar el número de vocales de la siguiente frase: No hay mal que dure cien años
fraseinicial='no hay mal que dure cien años'
numa=fraseinicial.count('a')
nume=fraseinicial.count('e')
numi=fraseinicial.count('i')
numo=fraseinicial.count('o')
numu=fraseinicial.count('u')
print('la vocal a sale: ',numa,' veces')
print('la vocal e sale: ',nume,' veces')
print('la vocal i sale: ',numi,' veces')
print('la vocal o sale: ',numo,' veces')
print('la vocal u sale: ',numu,' veces')